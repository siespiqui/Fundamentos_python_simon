#operadores de comparacion

print(1 == 1) #si es igual = true
print(2!= 1) #si es diferente = true
print(2 > 1) #si es mayor = true
print(1 < 2) #si es menor = true
print(1 >= 1) #si es mayor o igual = true
print(1 <= 1) #si es menor o igual = true

#Ahora con valores que no son iguales

print(1 == 4) #si no es igual = false
print(2!= 2) #si no es diferente = false
print(6 > 10) #si no es mayor = false
print(15 < 2) #si no es menor = false
print(1 >= 10) #si no es mayor o igual = false
print(10 <= 1) #si no es menor o igual = false

